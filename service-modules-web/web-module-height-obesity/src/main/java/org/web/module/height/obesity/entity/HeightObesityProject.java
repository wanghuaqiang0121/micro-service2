package org.web.module.height.obesity.entity;

import java.util.List;

import javax.validation.Valid;

public class HeightObesityProject {

   @Valid
   private UserHeightObesityProject userHeightObesityProject;

   @Valid
   private List<UserRecipesProject> userRecipesProject;

   @Valid
   private List<UserDrugsProject> userDrugsProject;

   public UserHeightObesityProject getUserHeightObesityProject() {
      return userHeightObesityProject;
   }

   public void setUserHeightObesityProject(UserHeightObesityProject userHeightObesityProject) {
      this.userHeightObesityProject = userHeightObesityProject;
   }

   public List<UserRecipesProject> getUserRecipesProject() {
      return userRecipesProject;
   }

   public void setUserRecipesProject(List<UserRecipesProject> userRecipesProject) {
      this.userRecipesProject = userRecipesProject;
   }

   public List<UserDrugsProject> getUserDrugsProject() {
      return userDrugsProject;
   }

   public void setUserDrugsProject(List<UserDrugsProject> userDrugsProject) {
      this.userDrugsProject = userDrugsProject;
   }

}
